char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if ('a' <= str[i] && str[i] <= 'z')
		{
			str[i] -= 'a';
			str[i] += 'A';
		}
		i++;
	}
	return (str);
}
