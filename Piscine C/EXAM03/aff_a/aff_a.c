#include <unistd.h>

int main(int argc, char **argv)
{
	if (argc == 2)
	{
		for (int i = 0; argv[i]; i++)
		{
			if (argv[1][i] == 'a')
			{
				write(1, "a\n", 2);
				return (0);
			}
		}
		write(1, "\n", 1);
	}
	else
	{
		write(1, "a\n", 2);
	}
	return (0);
}
