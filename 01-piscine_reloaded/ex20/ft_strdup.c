#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*str;
	int		i;

	if (src == NULL)
		return (src);
	i = 0;
	while (src[i])
		i++;
	str = (char *)malloc(i);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
