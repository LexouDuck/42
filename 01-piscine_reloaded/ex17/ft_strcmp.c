int	ft_strcmp(char *s1, char *s2)
{
	unsigned int	i;

	if (s1 == s2)
		return (0);
	if (s1 && s2)
	{
		i = 0;
		while (s1[i] && s2[i])
		{
			if (s1[i] != s2[i])
			{
				return (s1[i] - s2[i]);
			}
			i++;
		}
		return (s1[i] - s2[i]);
	}
	return ((s1 ? s1[0] : 0) -
			(s2 ? s2[0] : 0));
}
