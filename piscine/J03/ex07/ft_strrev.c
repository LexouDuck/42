char	*ft_strrev(char *str)
{
	int i;
	int j;
	int tmp1;
	int tmp2;

	i = 0;
	j = 0;
	while (str[j] != 0)
		j++;
	j--;
	while (i < j)
	{
		tmp1 = str[i];
		tmp2 = str[j];
		str[i] = tmp2;
		str[j] = tmp1;
		i++;
		j--;
	}
	return (str);
}
