#include <unistd.h>

int main(int argc, char **argv)
{
	char c;
	int i;

	if (argc == 2)
	{
		i = 0;
		while (argv[1][i]) i++;
		while (i--)
		{
			write(1, argv[1] + i, 1);
		}
	}
	c = '\n';
	write(1, &c, 1);
}
