char	*ft_strlowcase(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if ('A' <= str[i] && str[i] <= 'Z')
		{
			str[i] -= 'A';
			str[i] += 'a';
		}
		i++;
	}
	return (str);
}
