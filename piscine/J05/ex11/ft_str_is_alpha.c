int	ft_str_is_alpha(char *str)
{
	int i;

	i = -1;
	while (str[++i])
	{
		if (('a' <= str[i] && str[i] <= 'z') ||
			('A' <= str[i] && str[i] <= 'Z'))
			continue ;
		else
			break ;
	}
	if (str[i])
		return (0);
	else
		return (1);
}
